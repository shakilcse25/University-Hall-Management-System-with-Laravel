     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'seat_no' => 'required',
            'seat_cost' => 'required',
        ]);
        $seatcharge = new seatcharge();
        $seatcharge->seat_no = $request->seat_no;
        $seatcharge->seat_cost = $request->seat_cost;
        $seatcharge->save();
        $request->session()->flash('success', 'New Student seat cost Added Successfully!');
        return redirect()->back();
    }

